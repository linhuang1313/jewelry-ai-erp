/**
 * 获取今日金句（同一天返回相同的句子）
 */
export function getDailyQuote() {
  const today = new Date()
  const dayOfYear = Math.floor(
    (today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24)
  )
  const index = dayOfYear % QUOTES.length
  return QUOTES[index]
}
